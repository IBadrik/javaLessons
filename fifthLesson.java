public class fifthLesson {
    public static void main(String[] args) {
        float numOne = 1000000F;
        System.out.println(numOne / 3); // => 333333.34

        double numTwo = 1000000.0;
        System.out.println(numTwo / 3); // => 333333.3333333333
    }
}

